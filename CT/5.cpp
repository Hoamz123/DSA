void solve(){
    string s;cin >> s;
    int n = s.size();
    int dp[n][n];
    for(int len = 1;len <= n;len ++){
        for(int i = 0;i<n - len + 1;i++){
            int j = i + len - 1;
            if(len == 1) dp[i][j] = 1;
            else{
                if(s[i] == s[j]) dp[i][j] = dp[i+1][j-1] + 2;
                else dp[i][j] = max(dp[i+1][j],dp[i][j-1]);
            }
        }
    }
    cout << dp[0][n-1];   
}


