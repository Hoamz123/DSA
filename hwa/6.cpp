void solve(){
    int n,q;cin >> n >> q;
    vi a(n);
    unordered_map<int,vi> mp;
    mp.reserve(n * 2);
    FOR0(i,n) {
        cin >> a[i];
        mp[a[i]].push_back(i);
    }
    while(q--){
        int l,r,x;cin >> l >> r >> x;
        --l;--r;
        if(mp.count(x) == 0){
            //ko xuat hien trong mang
            cout << 0 << endl;
            continue;
        }
        const vi v = mp[x];
        int i = lower_bound(v.begin(),v.end(),l) - v.begin();
        int j = upper_bound(v.begin(),v.end(),r) - v.begin();
        cout << j - i << endl;
    }
}

