void seive(ll L,ll R){
    int prime[R - L + 1];
    for(ll i=0;i<R - L + 1;i++) prime[i] = 1;
    for(ll i=2;i * i <= R;i++){
        for(ll j = max(i * i,(L + i - 1) / i * i);j<=R;j+=i) prime[j - L] = 0;
    }
    for(ll i = max(1ll *2,L);i<=R;i++){
        if(prime[i - L]) cout << i << ' ';
    }
}

void solve(){
    ll L,R;cin >> L >> R;
    seive(L,R);
}

