void solve(){
    int n;cin >> n;
    vi a(maxn,0);
    vi cnt(maxn,0);
    int maxA = 0;
    FOR1(i,n){
        cin >> a[i];
        maxA = max(maxA,a[i]);
        cnt[a[i]]++;
    }
    for(int i = maxA;i>=1;i--){
        int dem = 0;
        for(int j = i;j <= maxA;j+=i){// j la boi cua i
            dem += cnt[j];//dem so lan xuat hien j (boi cua i) trong mang(neu co nhieu hon 2 lan xuat hien thi chung to co > 2 so chia het cho i)
            if(dem >= 2){
                cout << i;
                return;
            }
        }
    }
}

