//voi thoi gian x ta can bao nhieu con robot
int f(const vi &a,int x){
    int res = 0;
    for(int i=0;i<(int)a.size();i++){
        res += (a[i] + x - 1) / x;//lam tron nen
    }
    return res;
}

void solve(){
    ll n,m;cin >> n >> m;
    vi a(m,0);
    FOR0(i,m) {
        cin >> a[i];
    }
    ll L = 1,R = 1e14;
    int ans = 0;
    while(L <= R){
        ll mid = L + (R - L) / 2;
        ll res = f(a,mid);
        if(res <= n){
            ans = mid;
            R = mid - 1;
        }
        else L = mid + 1;
    }
    cout << ans;
}

int main(){
    faster();
    int t = 1;
    while(t--){
        solve();
    }
}
